from pathlib import Path

PACKAGE = "WP_COCKPIT_EMAIL_HTML_INLINE_STYLE_FIX"


def insert_before(path: Path, anchor: str, marker: str, block: str) -> None:
    text = path.read_text(encoding="utf-8")
    if marker in text:
        return
    if anchor not in text:
        raise SystemExit(f"anchor missing in {path}: {anchor}")
    text = text.replace(anchor, block.rstrip() + "\n\n" + anchor, 1)
    path.write_text(text, encoding="utf-8")


def append_once(path: Path, marker: str, block: str) -> None:
    text = path.read_text(encoding="utf-8")
    if marker in text:
        return
    path.write_text(text.rstrip() + "\n\n" + block.rstrip() + "\n", encoding="utf-8")


